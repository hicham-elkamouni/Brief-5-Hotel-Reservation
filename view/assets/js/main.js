function sign_up() {
    console.log(5);
    if ((document.getElementById("sign_up_btn").click = true)) {
        document.getElementById("sign-in1").style.display = "none";
        document.getElementById("sign-up1").style.display = "block";
    }
}

function sign_in() {
    if ((document.getElementById("sign_in_btn").click = true)) {
        document.getElementById("sign-in1").style.display = "block";
        document.getElementById("sign-up1").style.display = "none";
    }
}